  _ChatScreenState createState() => _ChatScreenState();
}

class _ChatScreenState extends State<ChatScreen> {
  @override
  initState() {
    super.initState();
    getCurrentUser();
  }

  final _auth = FirebaseAuth.instance;
  User? loggedIn;

  void getCurrentUser() {
    try {
      final User? usuario = _auth.currentUser;
      if (usuario != null) {
        loggedIn = usuario;
        print(loggedIn?.email);
      }
    } catch (e) {
      print(e);
    }
  }

